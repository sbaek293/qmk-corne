static void animate_luna(int LUNA_X, int LUNA_Y) {
    if (isJumping || !showedJump) {
        oled_set_cursor(LUNA_X, LUNA_Y + 2);
        oled_write("     ", false);
        oled_set_cursor(LUNA_X, LUNA_Y - 1);
        showedJump = true;
    } else {
        oled_set_cursor(LUNA_X, LUNA_Y - 1);
        oled_write("     ", false);
        oled_set_cursor(LUNA_X, LUNA_Y);
    }

    current_frame = (current_frame + 1) % 2;

    if (isBarking) {
        oled_write_raw_P(luna_bark[abs(1 - current_frame)], ANIM_SIZE);
    } else if (isSneaking) {
        oled_write_raw_P(luna_sneak[abs(1 - current_frame)], ANIM_SIZE);
    } else if (current_wpm <= MIN_WALK_SPEED) {
        oled_write_raw_P(luna_sit[1], ANIM_SIZE);
    } else if (current_wpm <= MIN_RUN_SPEED) {
        oled_write_raw_P(luna_walk[abs(1 - current_frame)], ANIM_SIZE);
    } else {
        oled_write_raw_P(luna_run[abs(1 - current_frame)], ANIM_SIZE);
    }
}

static void render_luna(int LUNA_X, int LUNA_Y) {
    if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) {
        anim_timer = timer_read32();
        animate_luna(LUNA_X, LUNA_Y);
    }
}

